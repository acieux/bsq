int	ft_strcmp(char *s1, char *s2)
{
	int	inc;

	inc = 0;
	while (s1[inc] != '\0' || s2[inc] != '\0')
	{
		if ((s1[inc]) != (s2[inc]))
			return (s1[inc] - s2[inc]);
		inc++;
	}
	return (0);
}
